    <h2>Egg Tarts</h2>

    <img src="/images/egg-tarts.webp" alt="14 golden-brown egg tarts in tins on a baking sheet." width="320px" height="320px" />

    <div>
      <p>12 servings</p>
      <h3>Ingredients</h3>
      <ul><li>11 tablespoons unsalted butter</li><li>1/2 cup granulated sugar</li><li>1/2 teaspoon salt</li><li>1 large egg</li><li>1 1/2 cups all purpose flour</li><li>1/2 cup granulated sugar</li><li>1/2 teaspoon cornstarch</li><li>1/8 teaspoon salt</li><li>3/4 cup milk</li><li>2/3 cup heavy cream</li><li>8 egg yolks</li><li>1 teaspoon vanilla extract</li></ul>
    </div>

    <div>
      <h3>Instructions</h3>

      <ol><li>Adjust oven rack to lower-middle position, place baking stone or sheet pan on rack, and heat oven to 375Â°. Lightly grease a 12 cup muffin tin. Stir butter, sugar, and salt in a large bowl until combined. Stir in egg, and then flour until completely combined. Divide dough into 16 portions and roll into balls.</li><li>Press a dough ball into each cup, pressing dough up sides to about 1/4 inch from top of cup, and maintaining a thickness about 1/8-inch thick. Chill for 30 minutes.</li><li>Whisk sugar, cornstarch, and salt in a medium bowl. Whisk in milk, cream, yolks, and vanilla until combined.</li><li>Fill each cup with custard to about 1/8-inch from top of crust (don't overfill). Bake until crust is golden and custard is just set, about 20 minutes. Let tarts cool in pan for 15 minutes then carefully transfer to wire rack to cool. Let pan cool, then repeat with remaining dough and custard. Serve room temperature or chilled.</li></ol>
    </div>

