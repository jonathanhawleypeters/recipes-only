  <div>
    <p>4 servings</p>
    <h3>Ingredients</h3>
    <ul><li>Celery, chopped (generous amount)</li><li>Pre-cooked and rinsed barley</li><li>Beef broth</li><li>Chicken broth</li><li>Carrots, peeled and diced</li><li>Oil</li><li>Onion, coarsely chopped</li><li>Stir-fry beef or sausage</li><li>Garlic cloves</li><li>Fresh parsley</li><li>White wine (optional)</li><li>Tomato sauce or paste (optional)</li><li>Vegeta seasoning or salt</li><li>Parmigiano-Reggiano or Pecorino cheese, for serving</li></ul>
  </div>

  <div>
    <h3>Instructions</h3>

    <ol><li>Prepare the Soup Base: In a large pot, combine the beef and chicken broth. Add the pre-cooked barley and a generous amount of chopped celery. Bring to a simmer.</li><li>Peel and dice the carrots, then add them to the pot. Let the mixture cook while you prepare the condiment.</li><li>Prepare the Condiment: In a small blender or food processor, combine a little oil, onion, stir-fry beef (chopped), garlic cloves, and fresh parsley. Pulse until coarsely chopped and well combined.</li><li>Transfer the mixture to a frying pan and cook for a few minutes.</li><li>If desired, add a small amount of white wine and a spoonful of tomato sauce or paste for color and flavor.</li><li>Combine and Season: Add the cooked condiment mixture into the soup pot.</li><li>Season with a small amount of Vegeta (be careful as it's salty) and stir well.</li><li>Let the soup simmer for a few more minutes until all flavors are combined.</li><li>Serve: Ladle the hot soup into bowls and top with a sprinkle of cheese (like Parmigiano-Reggiano or Pecorino).</li></ol>
  </div>

