    <h2>Chinese hot oil noodles - 油泼面</h2>

    <img class="hero-image" src="/images/yo-po-mian.webp?h=640;320;160&aspect=1:1" alt="A hot bowl of noodles and vegetables, surrounded by small dishes of peppercorns and salt." />

    <div>
      <p>1 serving</p>
      <h3>Ingredients</h3>
      <ul><li>4 ounces dried wheat noodles</li><li>1 handful of leafy greens (choy sum, spinach, or baby bok choy)</li><li>1 teaspoon light soy sauce</li><li>1 teaspoon dark soy sauce</li><li>1/2 teaspoon Chinese black vinegar</li><li>red pepper flakes</li><li>salt</li><li>chopped scallion</li><li>chopped cilantro</li><li>2 cloves garlic</li><li>2 1/2 tablespoons oil</li></ul>
    </div>

    <div>
      <h3>Instructions</h3>

      <ol><li>Boil the noodles according to package directions until al dente. In the same pot, blanch the leafy greens until cooked through. Drain.</li><li>Add the cooked noodles and greens to a heatproof bowl, along with the light soy sauce, dark soy sauce, vinegar, crushed red pepper flakes, salt, scallion, cilantro, and minced garlic.</li><li>In a small pot, heat the oil until shimmering. Carefully pour the hot oil over the bowl of noodles, and mix everything together. Serve!</li></ol>
    </div>

