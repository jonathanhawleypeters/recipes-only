    <h2>Green Bean Casserole</h2>

    <img class="hero-image" src="/images/green-bean-casserole.webp?h=640;320;160&aspect=1:1" alt="A casserole dish on a counter containing a green bean, mushroom casserole with fried onions over the top." />

    <div>
      <p>6 servings</p>
      <h3>Ingredients</h3>
      <ul><li>2 tablespoons olive oil</li><li>1 medium onion, thinly sliced</li><li>Kosher salt, freshly ground black pepper</li><li>1 pound mushrooms, such as cremini, button, oyster or maitake, quartered</li><li>1 pound green beans, stems trimmed, cut in half or coarsely chopped, if you like</li><li>2 tablespoons unsalted butter</li><li>2 tablespoons flour</li><li>1 3/4 cups whole milk</li><li>2 tablespoons soy sauce or tamari</li><li>1 clove garlic, finely grated or chopped</li><li>An undetermined amount of French’s Onions, from the container</li></ul>
    </div>

    <div>
      <h3>Instructions</h3>

      <ol><li>Heat olive oil in a large (at least 8”) skillet over medium–high heat. Add onions and season with salt and pepper. Cook, stirring occasionally (I use a wooden spoon this whole time), until they start to soften and brown, 10–12 minutes.</li><li>Reduce heat to medium, add mushrooms and season again with salt and pepper. Cook, stirring occasionally until they shrink, soften, brown and concentrate their flavors, another 10–12 minutes (the onions will be very dark and caramelized at this point, too, which is what we want).</li><li>Add green beans (the skillet will be a little full now, but just give it time, the green beans will soften and shrink as they cook), season with salt and pepper and toss to coat. Cook, stirring occasionally, until they’re bright green and totally tender, 8–10 minutes.</li><li>Add butter and let it melt in the skillet. Sprinkle flour over everything and stir for 2 or 3 minutes to toast in the fat and browned bits of the skillet. Slowly add milk, letting it thicken between stirs, until it’s all added. Add soy sauce and garlic, stirring to blend. Let the gravy come to a simmer, thickening in the skillet around the vegetables, making sure everything is evenly dispersed and coated. Season again with salt and more pepper.</li><li>Preheat oven to 425°. Top green beans and mushrooms with French’s Onions from a container and bake until golden brown and bubbly around the edges, 20–25 minutes.</li></ol>
    </div>

